static int32_t sh_ois_fw_driver_i2c_probe_0(struct i2c_client *client,
	const struct i2c_device_id *id)
{
	int32_t rc = 0;
	sh_ois_info_t *p_sh_ois_info = &sh_ois_info_00;

	SHCAMPDAF_INFO("\n\nEnter: sh_ois_fw_driver_i2c_probe_0\n");

	memset(p_sh_ois_info, 0, sizeof(sh_ois_info_t));
	mutex_init(&sh_ois_info_00.shcampdaf_mutex);

	if (!i2c_check_functionality(client->adapter, I2C_FUNC_I2C)) {
		pr_err("%s %s i2c_check_functionality failed\n",
			__func__, client->name);
		rc = -EFAULT;
		return rc;
	}
	
	mutex_init(&shcampdaf_mutex_00);

	i2c_set_clientdata(client, p_sh_ois_info);

	/* Initialize sensor device type */
	if (p_sh_ois_info != NULL) {
		p_sh_ois_info->client = client;
		p_sh_ois_info->index = 0;
	}
	
	sh_ois_init(p_sh_ois_info);
	
	rc = alloc_chrdev_region(&dev, 0, 1, "back_mult_camera_caldata.bin");
	if (rc != 0) {
		pr_err("alloc_chrdev_region = %d\n", rc);
		return -EFAULT;
	}
	cdev_init(&c_dev, &back_mult_camera_caldata_fops);
	c_dev.owner = THIS_MODULE;
	
	rc = cdev_add(&c_dev, dev, 1);
	if (rc != 0) {
		pr_err("cdev_add = %d\n", rc);
		return -EFAULT;
	}
	
	my_class = class_create(THIS_MODULE, "caldata");
	
	if (IS_ERR(my_class)) {
		pr_err("class_create error\n");
		cdev_del(&c_dev);
		unregister_chrdev_region(dev, 1);
		return -EFAULT;
	}
	device_create(my_class, NULL, dev, NULL, "back_mult_camera_caldata.bin");
	
	tof_hw_revision = sh_boot_get_hw_revision();

	return rc;
}

