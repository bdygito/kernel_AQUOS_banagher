static int32_t sh_ois_init(sh_ois_info_t *p_sh_ois_info)
{
	int i, rc = 0;
	struct sh_ois_soc_private  *soc_private = NULL;
	struct device_node *of_node = NULL;
	struct cam_hw_soc_info *soc_info = &p_sh_ois_info->soc_info;

	SHCAMPDAF_INFO("p_sh_ois_info->index = %d\n", p_sh_ois_info->index);

	soc_info->dev = &(p_sh_ois_info->client->dev);
	soc_info->dev_name = p_sh_ois_info->client->name;
	SHCAMPDAF_INFO("soc_info->dev_name = %s\n", soc_info->dev_name);
	soc_private = kzalloc(sizeof(struct sh_ois_soc_private),
		GFP_KERNEL);
	if (!soc_private) {
		rc = -ENOMEM;
		pr_err("%s:%d soc_private == NULL\n", __func__, __LINE__);
		return rc;
	}
	soc_info->soc_private = soc_private;
	
	cam_soc_util_get_dt_properties(soc_info);
	
	of_node = p_sh_ois_info->soc_info.dev->of_node;
	
	cam_get_dt_power_setting_data(of_node, soc_info, &(soc_private->power_info));
	
	if (!soc_info->gpio_data) {
		SHCAMPDAF_INFO("No GPIO found");
		return 0;
	}

	if (!soc_info->gpio_data->cam_gpio_common_tbl_size) {
		SHCAMPDAF_INFO("No GPIO found");
		return -EINVAL;
	}

	rc = cam_sensor_util_init_gpio_pin_tbl(soc_info,
		&soc_private->power_info.gpio_num_info);
	if ((rc < 0) || (!soc_private->power_info.gpio_num_info)) {
		SHCAMPDAF_INFO("No/Error GPIOs");
		return -EINVAL;
	}
	
	for (i = 0; i < soc_info->num_clk; i++) {
		SHCAMPDAF_INFO("clk_name[%d]: %s\n",i, soc_info->clk_name[i]);
		soc_info->clk[i] = devm_clk_get(soc_info->dev,
			soc_info->clk_name[i]);
		if (!soc_info->clk[i]) {
			pr_err("get failed for %s",
				soc_info->clk_name[i]);
			rc = -ENOENT;
			return rc;
		}
	}
	
	ois_debugfs_init(p_sh_ois_info);

	return rc;
}

